package org.catrobat.catroid.gui.tabpager;

import android.support.v4.app.Fragment;
import android.support.v4.app.FragmentManager;
import android.support.v4.app.FragmentPagerAdapter;

import java.util.List;

public class SpriteFragmentPager extends FragmentPagerAdapter {

	public static final String TAG = FragmentPagerAdapter.class.getSimpleName();

	private List<String> titles;
	private List<Fragment> fragments;

	public SpriteFragmentPager(FragmentManager fm, List<String> titles, List<Fragment> fragments) {
		super(fm);
		this.titles = titles;
		this.fragments = fragments;
	}

	@Override
	public Fragment getItem(int position) {
		return fragments.get(position);
	}

	@Override
	public int getCount() {
		return 3;
	}

	@Override
	public CharSequence getPageTitle(int position) {
		return titles.get(position);
	}
}
